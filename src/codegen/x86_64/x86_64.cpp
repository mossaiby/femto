std::string X86_64CodeGen::generate(lir::LIRModule& mod) {
    out_ << "; Generated by femto compiler\n";
    out_ << "section .data\n";
    emit_data_section(mod);

    out_ << "\nsection .text\n";
    out_ << "global _start\n\n";

    for (auto& func : mod.functions) {
        emit_function(func);
    }

    // Entry point
    out_ << "_start:\n";
    out_ << "    call main\n";
    out_ << "    mov rdi, rax\n";
    out_ << "    mov rax, 60\n";
    out_ << "    syscall\n\n";

    return out_.str();
}

